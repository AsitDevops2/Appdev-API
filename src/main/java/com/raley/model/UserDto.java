package com.raley.model;

public class UserDto {

	private Integer id;
	private String firstName;
	private String lastName;
	private String email;
	private String password;
	private long mobileNo;
	private int pin;
	private String country;
	private String state;
	private String city;
	private String dept;
	private String addrLand1;
	private String addrLand2;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public long getMobileNo() {
		return mobileNo;
	}

	public void setMobileNo(long mobileNo) {
		this.mobileNo = mobileNo;
	}

	public String getAddrLand1() {
		return addrLand1;
	}

	public void setAddrLand1(String addrLand1) {
		this.addrLand1 = addrLand1;
	}

	public String getAddrLand2() {
		return addrLand2;
	}

	public void setAddrLand2(String addrLand2) {
		this.addrLand2 = addrLand2;
	}

	public int getPin() {
		return pin;
	}

	public void setPin(int pin) {
		this.pin = pin;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getDept() {
		return dept;
	}

	public void setDept(String dept) {
		this.dept = dept;
	}

	

}
